import { Model, DataTypes } from 'sequelize';
import db from '.';
// import OtherModel from './OtherModel';

class Team extends Model {
  declare id: number;
  declare teamName: string;
}

Team.init({
  id: {
    primaryKey: true,
    autoIncrement: true,
    type: DataTypes.INTEGER,
    allowNull: false,
  },
  teamName: {
    type: DataTypes.STRING(255),
    allowNull: false,
  },
}, {
  tableName: 'teams',
  timestamps: false,
  underscored: true,
  sequelize: db,
});

